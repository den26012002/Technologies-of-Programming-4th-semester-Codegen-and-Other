package den26012002.catsentities;

public class Color {
    private int _r;
    private int _g;
    private int _b;

    public Color(int r, int g, int b) {
        _r = r;
        _g = g;
        _b = b;
    }

    public int getR() {
        return _r;
    }

    public int getG() {
        return _g;
    }

    public int getB() {
        return _b;
    }
}
